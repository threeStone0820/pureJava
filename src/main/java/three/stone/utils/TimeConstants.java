package three.stone.utils;

public final class TimeConstants {
    public static final long NANOS_IN_MILLI = 1000000L;

    public static final long MILLISECONDS = 1;
    public static final long SECONDS = 1000 * MILLISECONDS;
    public static final long MINUTES = 60 * SECONDS;
    public static final long HOURS = 60 * MINUTES;
    public static final long DAYS = 24 * HOURS;
    public static final long WEEKS = 7 * DAYS;
    public static final long YEARS = 365 * DAYS;
}
